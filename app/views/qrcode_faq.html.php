      <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#question_8" aria-expanded="false" aria-controls="question_8">
        Comment fonctionne la dématérialisation ?
      </button>
    </h2>
    <div id="question_8" class="accordion-collapse collapse">
      <div class="accordion-body">
        Présenter un QR Code, un code barre...
        Le mode de dématérialisation est libre mais chaque cuvée doit afficher les informations spécifiques à la cuvée et avoir ses propres déclarations nutritionnelles et liste des ingrédients.

        La liste d'ingrédients doit être précédée de la mention "INGRÉDIENTS".

        Les deux nouvelles mentions, même si elles sont dématérialisées, doivent être notées sur l'étiquette à côté du système de dématérialisation avec les mentions "ingrédients" et "déclaration nutritionnelle".
      </div>
    </div>
  </div>


