        Et les auxiliaires technologiques ?
      </button>
    </h2>
    <div id="question_11" class="accordion-collapse collapse">
      <div class="accordion-body">
        Un auxiliaire technologique est une substance utilisées dans le processus de fabrication de "denrées alimentaires transformées" pour répondre à un certain objectif technologique pendant le traitement ou la transformation.

        Ils ne sont pas obligatoirement inscrits dans la liste d'ingrédients sauf s'ils sont allergènes.
        <div class="mt-3 text-end">
            <small>Article 3 du Règlement UE 1333/2008</small>
        </div>
      </div>
    </div>
